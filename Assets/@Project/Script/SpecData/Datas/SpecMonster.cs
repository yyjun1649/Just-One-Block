
public class SpecMonster
{
    public int fieldID;
    public int level;
    public int damage;
    public int attackSpeed;
    public int health;
    public float speed;
    public float attackRange;

    public SpecMonster(int fieldID, int level, int damage,int attackSpeed, int health, float speed, float attackRange)
    {
        this.fieldID = fieldID;
        this.level = level;
        this.damage = damage;
        this.attackSpeed = attackSpeed;
        this.health = health;
        this.speed = speed;
        this.attackRange = attackRange;
    }
}
